def sum(first_value, second_value):
    result = first_value + second_value
    return round(result, 8)

def sub(first_value, second_value):
    result = first_value - second_value
    return round(result, 8)

def mult(first_value, second_value):
    result = first_value * second_value
    return round(result, 8)

def div(first_value, second_value):
    result = first_value / second_value
    return round(result, 8)